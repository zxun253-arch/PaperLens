export function readingNotePrompt(paperText: string, isTruncated: boolean) {
  return `你是一名严谨的中文科研阅读助手。请基于下面提供的论文内容，整理一份结构化中文精读笔记。

## 格式要求（非常重要）
- 使用 **Markdown 格式** 输出
- 每个大节使用 ## 或 ### 标题
- 关键词和术语使用 **加粗**
- 列表使用 - 或 1. 格式
- 对比内容使用 | 表格 |
- 公式使用 \`\` 或 \`\`\`
- 段落之间用空行，不要堆成一大段

## 内容要求
- 必须忠实论文原文，不要脱离论文内容自由发挥
- 不允许编造不存在的实验、数据、图表或结论
- 如果论文内容没有相关信息，请写"原文未明确说明"
- 表达自然清晰，适合中文科研学习笔记
- 不要写空泛总结
- 回答时尽量引用依据所在的 chunk_index 和 section_title
${isTruncated ? "- 注意：以下为论文节选内容，不是全文。请只基于已提供内容整理笔记。" : ""}

## 输出结构
### 1. 论文基本信息
标题、作者、年份、期刊/会议

### 2. 研究背景
该论文试图解决什么问题？已有研究的不足是什么？

### 3. 核心创新点
作者提出的方法/框架的核心思路

### 4. 方法细节
关键算法、模型架构、实验设计

### 5. 关键结果
主要实验结果和发现（用表格呈现最好）

### 6. 结论与局限性
作者的主要结论和指出的局限性

### 7. 对读者的启发
可借鉴的思路或方法

## 论文内容
${paperText}`;
}
