export function literatureReviewPrompt(
  paperText: string,
  isTruncated: boolean,
) {
  return `你是一名严谨的中文科研写作助手。请根据下面提供的论文内容，整理这篇论文对文献综述写作的价值。

重要要求：
- 必须忠实原文，不能凭空扩展。
- 不能夸大论文贡献。
- 如果原文没有明确说明，请写“原文未明确说明”。
- 需要区分“原文明确内容”和“可作为写作参考的归纳”。
- 使用中文回答。
- 回答时尽量引用依据所在的 chunk_index 和 section_title。
${isTruncated ? "- 注意：以下为论文节选内容，不是全文。请只基于已提供内容整理。" : ""}

请输出：
1. 这篇论文属于什么研究方向
2. 这篇论文可以支撑什么背景论述
3. 这篇论文可以支撑什么研究现状
4. 这篇论文的创新点如何概括
5. 这篇论文的不足如何表述
6. 可以放在文献综述哪一类小节中
7. 可用于论文中的中文表述草稿

论文内容：
${paperText}`;
}
