export function literatureReviewPrompt(
  paperText: string,
  isTruncated: boolean,
) {
  return `你是一名严谨的中文科研写作助手。请根据下面提供的论文内容，整理这篇论文对文献综述写作的价值。

## 格式要求（非常重要）
- 使用 **Markdown 格式** 输出
- 每个部分使用 ### 标题
- 关键词使用 **加粗**
- 列表使用 - 格式
- 段落之间用空行分隔

## 内容要求
- 必须忠实原文，不能凭空扩展
- 不能夸大论文贡献
- 如果原文没有明确说明，请写"原文未明确说明"
- 需要区分"原文明确内容"和"可作为写作参考的归纳"
- 使用中文回答
- 尽量引用依据所在的 chunk_index 和 section_title
${isTruncated ? "- 注意：以下为论文节选内容，不是全文。请只基于已提供内容整理。" : ""}

## 输出结构
### 1. 研究方向
### 2. 可支撑的背景论述
### 3. 可支撑的研究现状
### 4. 创新点概括
### 5. 不足与局限
### 6. 适合放在文献综述的哪一类小节
### 7. 可用于论文中的中文表述草稿

## 论文内容
${paperText}`;
}
